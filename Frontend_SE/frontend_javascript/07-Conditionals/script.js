// Conditionals
    // if statements let us execute code under certain condition 
        // if / else 

function compare(x,y){
    if(x >y){
        console.log(x , "is greater than", y);
    }
    else if(x < y){
        console.log(x , "is less than ", y);
    }
    else{
        console.log(x ,"x is equal to ",y);
    }
}


compare(2,2);